package com.springsamples.entities;

public class CompanyData {
	private String companycode;
	private String paymentterms;
	private String legacynumber;
	private String paymentBlock;
	public String getCompanycode() {
		return companycode;
	}
	public void setCompanycode(String companycode) {
		this.companycode = companycode;
	}
	public String getPaymentterms() {
		return paymentterms;
	}
	public void setPaymentterms(String paymentterms) {
		this.paymentterms = paymentterms;
	}
	public String getLegacynumber() {
		return legacynumber;
	}
	public void setLegacynumber(String legacynumber) {
		this.legacynumber = legacynumber;
	}
	public String getPaymentBlock() {
		return paymentBlock;
	}
	public void setPaymentBlock(String paymentBlock) {
		this.paymentBlock = paymentBlock;
	}

	
	
	
}
